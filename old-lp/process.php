    <div id="steps">

        <div class="container">

            <div class="col-md-1"></div>

            <div class="col-md-10">

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">1</div>

                    <div class="col-xs-10 col-md-11">
                    
                        <h3>Introduction</h3>

                        <p>The client reads the Canada Immigration preliminary package carefully and follows the instructions as contained therein.</p>

                    </div>

                </div>

                <div class="row">                

                    <div class="col-xs-2 col-md-1 num">2</div>

                    <div class="col-xs-10 col-md-11">
                    
                        <h3>Application Forms</h3>

                        <p>The client fills out the Government of Canada Application forms as forwarded with Abrams & Krochak's preliminary package.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">3</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Complete Application Forms</h3>

                        <p>The client sends his/her completed Canada Immigration Application forms to Abrams & Krochak for review. The client is not to send any other documents at this time.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">4</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Application Forms Computer Generated/Express Entry Online Profile Created</h3>

                        <p>The firm reviews the client's Canada Immigration Application forms. If the forms are complete, in the case of Business Class and Family Class applicants, the firm will computer generate the forms and e-mail them back to the client for approval and signature. In the case of Federal Skilled Worker Class/Federal Skilled Trades Class/Canadian Experience Class applicants, the firm will create the applicant’s Express Entry Online Profile.  In all cases, if further information/clarifications are required from the client, the client will be notified, via e-mail.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">5</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Send Documents to our Office</h3>

                        <p>In the case of Business Class and Family Class applicants, the client sends all of his/her documents and signed computer generated Canada Immigration Application forms to the firm by courier. The client must also send the Government of Canada's fees to Abrams & Krochak at this time.  Minus the computer generated Canada Immigration Application forms, the same holds true for Federal Skilled Worker Class/Federal Skilled Trades Class/Canadian Experience Class applicants, if invited by Citizenship and Immigration Canada to apply for Permanent Residence in Canada.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">6</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Legal Arguments Prepared</h3>

                        <p>In the case of Business Class and Family Class applicants, if the client's documents are acceptable, the firm will prepare detailed written, legal arguments to accompany the client's Canada Immigration Application package and e-mail the written, legal arguments to the client for the client's approval prior to inclusion with the Canada Immigration Application package.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">7</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Send Your Application Package to Visa Office</h3>

                        <p>In the case of Business Class and Family Class applicants, when the client approves the written, legal arguments, the client's entire Canada Immigration Application Package inclusive of the written, legal arguments will be sent to the Canadian visa office, via courier, for processing.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">8</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Acknowledgment Received</h3>

                        <p>The Canada visa office or processing centre will acknowledge receipt of the Canada Immigration Application and may request more documents.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">9</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Interview Date/Waiver Notification</h3>

                        <p>The Canada visa office or processing centre will inform Abrams & Krochak of the client's interview date or waiver of interview. Additional documents may also be requested at this time. If the interview is waived, the client must undergo the Canada Immigration Medical Examination, immediately (if he/she has not already done so, as is the case with Family Class applicants). If an interview is required, the client should undergo the Canada Immigration Medical Examination approximately one (1) month prior to the interview.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">10</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Interview Preparation</h3>

                        <p>If an interview is required, Abrams & Krochak's interview preparation package is sent to the client, via e-mail, which includes sample interview questions.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">11</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Interview Answer Analysis</h3>

                        <p>If an interview is required, the client submits answers to Abrams & Krochak's sample interview questions to the firm for review and analysis.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">12</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Interview</h3>

                        <p>If an interview is required, the client attends the interview at the Canadian visa office.</p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">13</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Decision</h3>

                        <p>A decision is made by the Canadian visa office or processing centre. If the decision is positive, the Government of Canada's Right of Permanent Residence fees must be paid and the client will be asked to forward his/her passport to the Canadian visa office for immigrant visa insertion.
                        </p>

                    </div>

                </div>

                <div class="row">

                    <div class="col-xs-2 col-md-1 num">14</div>

                    <div class="col-xs-10 col-md-11">

                        <h3>Completion</h3>

                        <p>If the decision is positive, the passport is returned to the client with immigrant visa affixed therein. The immigrant visa will bear an expiration date and the client must arrive in Canada prior to the expiration date to become landed. Congratulations! If arriving in <a href="lic_toronto.php">Toronto</a>, the client is invited to visit <a href="contact.php">Abrams &amp; Krochak's office</a> to receive free settlement advice.</p>

                    </div>

                </div>

            </div>

            <div class="col-md-1"></div>

