                <h1 class="text-center">How to Immigrate to Canada:<br>Key Steps in the Canadian Immigration Process</h1>

                <div class="divider"></div>                

            </div>

        </div>

        <div class="row">

            <div class="col-md-8 col-md-offset-2"><p class="text-center">When a Canada Immigration applicant becomes a client of Abrams &amp; Krochak, the very first thing he/she will receive from the firm is the firm's Canada Immigration preliminary package, which contains all of the necessary forms, document checklists, instructions and information to enable the client to commence the Canadian Immigration Application process with Abrams & Krochak's assistance. Set out, below, are the subsequent steps that clients should follow to ensure a smooth and successful Canada Immigration Application process. </p></div>
            
