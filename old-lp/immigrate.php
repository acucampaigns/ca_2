                <h3>Canadian Immigration Laws: Permanent Residence Status</h3>

                <p>Obtaining "permanent resident status of Canada" is also known as "Immigration to Canada" or becoming a "landed immigrant of Canada". The successful end result of the Canada Immigration Application process is the issuance of a Canada immigrant visa, which confers permanent status upon the applicant who, as a permanent resident, will enjoy all the same rights and privileges as those of a Canadian citizen (i.e. free health care, free elementary and secondary education, subsidized post-secondary education, the right to <a href="employment.php">work in Canada</a>) with just a few exceptions.</p>

                <p>Unlike Canadian citizens,</p>

                <ol style="list-style-type:lower-roman">
                    <li>a permanent resident cannot vote;</li>
                    <li>a permanent resident cannot hold a Canadian passport; and</li>
                    <li>a permanent resident can be deported for certain criminal convictions.</li>
                </ol>

                <p>A Canadian permanent resident is eligible to apply for Canadian Citizenship after residing in Canada for at least a total of 1095 days within any consecutive four (4) year period of time after receiving Canadian Permanent Residence. One is eligible to apply for a Canadian passport after obtaining Canadian Citizenship.</p>

                <p>Pursuant to the <em>Immigration and Refugee Protection Act</em> of Canada, a permanent resident must reside in Canada for a total of at least 730 days within the five (5) years prior to the date of expiration on his/her Permanent Resident Card in order to be eligible to renew the Permanent Resident Card, subject to certain exceptions.</p>

                <p>Persons to whom a Canadian immigrant visa has been issued must present themselves to a Canada Immigration officer at one of the official ports of entry of Canada in order to become a permanent resident.</p>

                <p>To be eligible to become a permanent resident of Canada, the aspiring resident must meet the requirements of one (1) of five (5) classes of Immigration: the Federal Skilled Worker Class, the Federal Skilled Trades Class, the Canadian Experience Class, the Family Class or the Business Class OR qualify as a provincial nominee.</p>

