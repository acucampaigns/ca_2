                <div class="divider"></div>
                                <p><strong>Please complete the following short form if:</strong></p>

                <p><strong>1. you wish to apply to sponsor a parent or grandparent for Immigration to Canada.</strong></p>

                <p><strong>OR</strong></p>
                
                <p><strong>2. you wish to immigrate to Canada pursuant to the Sponsorship Application of your child or grandchild, who is a Canadian citizen/permanent resident.</strong></p>

                <br />

                <p>Important information for the sponsor before completing the Parent/Grandparent Questionnaire:
                    You may be eligible to sponsor a parent or grandparent if:</p>

                <ul>
                    <li>the person you want to sponsor is a Member of the Family Class. If he or she is not, you will be found not to be a sponsor;
                    <li>you are eighteen (18) years of age or older;
                    <li>you are a Canadian citizen or permanent resident;
                    <li>you reside in Canada;
                    <li>you sign an undertaking promising to provide for the basic requirements of the person being sponsored and, if applicable, his or her family members;
                    <li>you and the sponsored person sign an Agreement that confirms that each of you understands your mutual obligations and responsibilities;
                    <li>you have an income that is at least equal to the minimum necessary income, the amount of which is published yearly by the Canadian government.
                </ul>

                <br />
