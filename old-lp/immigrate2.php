                <h2>Canada Immigration: Family Class</h2>

                <p>Foreign nationals may be selected as members of the Family Class on the basis of their relationship to a Canadian citizen or a permanent resident. Canadian citizens (including, in certain circumstances, those residing abroad with the intention to return to Canada to reside) and permanent residents (restricted to those residing in Canada, only), age eighteen (18) and over, may sponsor the following members of the Family Class (subject to meeting eligibility requirements) for Canada Immigration:</p>

                <ul>
                    <li>their spouse</li>
                    <li>their common-law/same-sex partner</li>
                    <li>their conjugal partner</li>
                    <li>their dependent children under age nineteen (19)</li>
                    <li>their dependent children age nineteen (19) and over (if they are mentally or physically disabled and dependent on their parent(s))</li>
                    <li>children adopted or to be adopted by them, or under their guardianship</li>
                    <li>parents</li>
                    <li>grandparents</li>
                    <li>other prescribed family members (i.e. last remaining family member under humanitarian and compassionate grounds where it is warranted).</li>
                </ul>

