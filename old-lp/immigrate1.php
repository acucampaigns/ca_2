                <h2>C.  Applying Under Express Entry</h2>

                <p>Citizenship and Immigration Canada has implemented a new method of processing candidates in the Federal Skilled Worker Class/Federal Skilled Trades Class/Canadian Experience Class for Immigration to Canada called Express Entry. The Express Entry system involves two (2) steps:</p>

                <h3>Step 1 Completion of Online Express Entry Profile</h3>

                <p>In order for an individual to complete an Online Express Entry Profile and possibly be invited to apply for Permanent Residence in Canada, he/she must qualify under the Federal Skilled Worker Class OR the Federal Skilled Trades Class OR the Canadian Experience Class.</p>


                <p>Candidates will complete an Online Express Entry profile. This is a secure form that gathers information about their:</p>
                <ul>
                    <li>skills,</li>
                    <li>work experience,</li>
                    <li>language ability,</li>
                    <li>education, and</li>
                    <li>other details that will help Citizenship and Immigration Canada assess their eligibility.</li>
                </ul>

                <p>Those who meet Citizenship and Immigration Canada's criteria will be accepted into a pool of candidates.</p>

                <p>Anyone who does not already have a job offer supported by a Labour Market Impact Assessment (LMIA) (if needed) or a nomination from a province or territory, must register with Employment and Social Development Canada's (ESDC) Job Bank. Job Bank will eventually help connect Express Entry candidates with eligible employers in Canada.</p>

                <p>Candidates are also encouraged to promote themselves to employers in other ways, such as using job boards, recruiters, etc.</p>

                <p>In most cases when there is a job being offered to a candidate, employers will need an LMIA from ESDC. The LMIA process ensures employers have made an effort to hire Canadians for available jobs. There will be no government fee payable for LMIA Applications that support only a Permanent Residence Application.  Government fees are payable for LMIA Applications that support a Work Permit Application.</p>

                <h3>Step 2 Highest Ranking Candidates in the Pool Will Be Invited to Apply for Permanent Residence</h3>

                <p>Candidates will be ranked against others in the pool using a point-based system called the Comprehensive Ranking System (CRS). The CRS will assess the profile information candidates submit, including:</p>

                <ul>
                    <li>skills,</li>
                    <li>work experience,</li>
                    <li>language ability,</li>
                    <li>education and other factors.</li>
                </ul>

                <p>A candidate can get additional points for:</p>

                <ul>
                    <li>a job offer supported by LMIA, or</li>
                    <li>a nomination by a province or territory.</li>
                </ul>

                <p>Citizenship and Immigration Canada will choose candidates from the pool through regular draws, choosing a pre-determined number of candidates with the highest scores from the pool and inviting them to apply for Permanent Residence in Canada.</p>

                <p>Draws will be made from the Express Entry pool on a regular basis. Every draw will have a set of instructions the electronic system will use to draw candidates from the pool. These instructions will be posted on the Citizenship and Immigration Canada website before each draw.</p>

                <p>These instructions will include:</p>

                <ul>
                    <li>the date of the next draw,</li>
                    <li>the number of candidates that will get an Invitation To Apply, and</li>
                    <li>if it applies, which specific Immigration program(s) will be included in the draw.</li>
                </ul>

                <p>If the instructions do not specify which Immigration program the candidates must be eligible for, the draw will be based on the CRS score.</p>

                <p>If someone is invited to apply, they will have sixty (60) days to submit an online Application for Permanent Residence.</p>

                <p>Citizenship and Immigration Canada will process the majority of complete Applications (meaning those with all the necessary supporting documents) in six (6) months or less.</p>

                <p>Candidates can stay in the pool for up to twelve (12) months. If they do not get an Invitation to Apply for Permanent Residence within twelve (12) months of submitting an Express Entry profile, they may submit a new profile. If they still meet the criteria, they can re-enter the pool. This will prevent backlogs and ensure quick processing times.</p>

