                <h2>A.  Meeting Eligibility Requirements</h2>

                <p>In order for an individual to meet eligibility requirements for the Federal Skilled Worker Class, he/she must have at least one (1) year of paid full-time experience (or an equal amount of paid part-time work experience) in a National Occupational Classification (NOC) occupation at Skill Type 0, A or B within the past ten (10) years.</p>

                <p>The NOC was developed by Employment and Social Development Canada (ESDC) to be a systematic cataloguing of occupations in the Canadian labour market. The NOC is divided into five (5) bands:</p>

                <div class="table-responsive">
                    <table class="table table-striped">
                    <tbody>
                    <tr>
                        <td>Skill Type 0</td>
                        <td>Management Occupations;</td>
                    </tr>
                    <tr>
                        <td>Skill Type A</td>
                        <td>which is primarily comprised of professional occupations;</td>
                    </tr>
                    <tr>
                        <td>Skill Type B</td>
                        <td>which consists of technical, skilled trades and paraprofessional occupations;</td>
                    </tr>
                    <tr>
                        <td>Skill Type C</td>
                        <td>which comprises occupations that mainly consist of intermediate level, clerical or supportive functions;</td>
                    </tr>
                    <tr>
                        <td>Skill Type D</td>
                        <td>which consists of elemental sales or service and primary labourer occupations.</td>
                    </tr>
                    </tbody>
                    </table>
                 </div>

                <br />

                <p>Only experience in Skill Type 0, A or B is considered relevant for applicants in the Federal Skilled Worker Class.</p>

                <p>To qualify for the Federal Skilled Worker Class, the experience in Skill Type 0, A or B must consist of actions described in the lead description and a substantial number of the duties described in the Main Duties section of the NOC, including all the essential duties.</p>


                <p>He/she must meet the minimum level of Canadian Language Benchmark (CLB) 7 in either English or French for all four (4) language abilities.</p>

                <h2>B.  Point Assessment</h2>

                <p>Assuming that an individual meets the aforementioned prerequisites, he/she is then assessed according to various selection criteria by Citizenship and Immigration Canada officials and he/she must attain a minimum score of sixty-seven (67) points overall to qualify for the Federal Skilled Worker Class.</p>

                <p>These selection criteria are as follows:</p>

