                <h1 class="text-center">Privacy Policy</h1>
            
                <div class="divider"></div>

                <p>Abrams &amp; Krochak Professional Corporation ("Abrams &amp; Krochak") recognizes the importance of privacy and the sensitivity of personal information. As lawyers we have a professional obligation to keep confidential all information we receive within a lawyer-client relationship. We are committed to protecting any personal information we hold. This Privacy Policy outlines how we manage your personal information and safeguard your privacy.</p>

                <h2>Your Privacy Rights</h2>

                <p>Abrams &amp; Krochak is responsible for the personal information we collect, use, maintain and disclose. To ensure this accountability, we have developed this policy, and trained our lawyers and support staff about our policies and practices.

                <p><strong>What is "Personal Information?"</strong><br />
                    Personal information is any information that identifies you, or by which your identity could be deduced. It does not include the name or business address or telephone number of an employee of an organization.

                <p><strong>Why Do We Collect Personal Information from You?</strong><br />
                    We collect personal information from our clients to:</p>

                <ul>
                    <li>provide legal services to you, in accordance with your instructions;</li>
                    <li>bill you for legal services rendered;</li>
                    <li>provide information to you about developments in the law; and </li>
                    <li>advise you of upcoming firm events.</li>
                </ul>

                <p><strong>How Do We Collect Your Personal Information?</strong><br />
                    We collect information only by lawful and fair means and not indiscriminately. We may collect personal information directly from you at the start of a retainer and in the course of our representation of you.</p>

                <p><strong>Consent</strong><br />
                    Consent for the collection, use and/or disclosure of personal information may be obtained orally or in writing and may be expressly given or implied. In determining how we obtain your consent, we will take into account the sensitivity of the personal information about you that we are collecting, using and/or disclosing.</p>

                <p><strong>Use of Your Information</strong><br />
                    When we use your personal information to provide legal advice and services to you, including for billing purposes and when we use your personal information to include you in any direct marketing activities, we will assume that you have consented to such use unless you specifically advise otherwise. If you tell us that you no longer wish to receive information about our services, or about new developments in the law, we will not send any further material.</p>

                <p>Abrams &amp; Krochak does not disclose your personal information to any third party to enable them to market their products and services.

                <p>
                    <strong>Disclosure of Your Personal Information</strong><br />
                    Under certain circumstances, Abrams &amp; Krochak may disclose your personal information:

                <ul>
                    <li>when we are required or authorized by law to do so, for example if a court issues a subpoena;</li>
                    <li>when you have consented to the disclosure;</li>
                    <li>when the legal services we are providing to you require us to give your information to a third party (for example, to Citizenship and Immigration Canada, Health Canada, Employment and Social Development Canada) your consent will be implied, unless you tell us otherwise;</li>
                    <li>where it is necessary to establish or collect fees;</li>
                    <li>if we engage a third party to provide administrative services to us (like computer back-up services or archival file storage) and the third party is bound by our privacy policy; or</li>
                    <li>if the information is already publicly known.</li>
                </ul>

                <p><strong>Updating Your Information</strong><br />
                    Since we use your personal information to provide legal services to you, it is important that the information be accurate and up-to-date.

                <p>If, during the course of the retainer, any of your information changes, please inform us so that we can make any necessary changes.

                <p>
                    <strong>Is My Personal Information Secure?</strong><br />
                    Abrams &amp; Krochak takes all reasonable precautions to ensure that your personal information is kept safe from loss, unauthorized access, modification or disclosure. Among the steps taken to protect your information are:</p>

                <ul>
                    <li>premises security;</li>
                    <li>restricted access to personal information stored electronically;</li>
                    <li>deploying technological safeguards like security software and firewalls to prevent hacking or unauthorized computer access;</li>
                    <li>internal password and security policies;</li>
                    <li>obligation of each lawyer to maintain client confidentiality under the Rules of Professional Conduct of the Law Society of Upper Canada;</li>
                    <li>all employees and third parties that have access to personal information collected by Abrams &amp; Krochak will be required to sign a confidentiality agreement that is in keeping with this Policy.</li>
                </ul>

                <p><strong>Access to Your Personal Information</strong><br />
                    You may ask for access to any personal information we hold about you. <br />
                    Summary information is available on request. More detailed requests that require archival or other retrieval costs may be subject to our normal professional and disbursement fees.</p>

                <p>
                    <strong>Correcting Errors</strong><br />If Abrams &amp; Krochak holds information about you and if you can establish that it is not accurate, complete and up-to-date, Abrams &amp; Krochak will take reasonable steps to correct it.</p>

                <p><strong>Can I be Denied Access to My Personal Information?</strong><br />
                    Your rights to access your personal information are not absolute.</p>


                <p>We may deny access when:</p>

                <ul>
                    <li>denial of access is required or authorized by law (for example, when a record containing personal information about you is subject to a claim of legal professional privilege by one of our clients);</li>
                    <li>information relates to existing or anticipated legal proceedings against you;</li>
                    <li>when granting you access would have an unreasonable impact on other people's privacy;</li>
                    <li>when to do so would prejudice negotiations with you;</li>
                    <li>to protect our firm's rights and property;</li>
                    <li>where the request is frivolous or vexatious.</li>
                </ul>

                <p>If we deny your request for access to, or refuse a request to correct information, we shall explain why.</p>

                <p>
                    <strong>Can I Request Anonymity?</strong><br />
                    Whenever it is legal and practical, we may elect to deal with general inquiries on an anonymous basis (for example, by accessing general information on our website).</p>


                <p><strong>How Long Do You Keep My Personal Information?</strong><br />
                    We keep your personal information as long as is reasonably necessary for us to complete our dealings with you, or as may be required by law, whichever is longer.</p>


                <p><strong>Web Site</strong><br />
                    Our website contains links to other sites, which are not governed by the privacy policy.</p>


                <p>We may also collect personal information on our website (www.akcanada.com). We only collect the information you provide to us voluntarily by sending us an e-mail message, completing forms or using the links contained on the website. Abrams &amp; Krochak collects and uses your name, address, telephone number, email address and other information you submit on or through the website for the purposes for which you provided the personal information, to respond to your inquiries and to provide you with updates and information that we think may be of interest to you. In providing Abrams &amp; Krochak with any personal information on or through this website, you consent to the collection and use of that information for these stated purposes. We currently do not use cookies on this website. "Cookies" are small text files that recognize repeat visitors and track their visits to a website.</p>


                <p>On our website, like most other commercial websites, we may monitor traffic patterns, site usage and related site information in order to optimize our web service. We may provide aggregated information to third parties, but these statistics do not include any identifiable personal information.</p>


                <p><strong>Communicating with Us</strong><br />
                    You should be aware that email is not a 100% secure medium, and you should be aware of this when contacting us to send personal or confidential information.</p>


                <p><strong>Changes to this Privacy Policy</strong><br />
                    Since Abrams &amp; Krochak regularly reviews all of its policies and procedures, we may change our Privacy Policy from time to time. Please review our website for updates.</p>


                <p><strong>Request for Access</strong><br />
                    If you have any questions, or wish to access your personal information, please write to our <a href="mailto:jabrams@akcanada.com">Privacy Officer</a>.</p>


                <p><strong>Inquiries / Complaints</strong><br />
                    If you have any questions or concerns about how your personal information has, is or will be handled by Abrams &amp; Krochak or if you would like to register a complaint, please contact our <a href="mailto:jabrams@akcanada.com">Privacy Officer</a>.</p>

