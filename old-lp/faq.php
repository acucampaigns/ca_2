                <h3>B. Permanent Status In Canada</h3>

                <a name="4"></a>
                
                <p><strong>1. What is permanent residence in Canada?</strong></p>
                
                <blockquote>
                    <p>Obtaining "permanent residence" or "permanent resident status" in Canada is also known as "immigrating to Canada" or becoming a "landed immigrant." The successful end result of the Immigration process is the issuance of an “immigrant visa” or “confirmation of permanent residence document”. Persons to whom an immigrant visa/confirmation of permanent residence document has been issued must present themselves to an Immigration officer at one of Canada's official ports of entry in order to become landed immigrants.</p>
                    <p><a href="#top">Back to top</a></p>
                </blockquote>

                <a name="5"></a>
                
                <p><strong>2. What benefits do I have if I am a permanent resident of Canada?</strong></p>
                
                <blockquote>
                    <p>Canadian permanent residents/landed immigrants and citizens enjoy all of the same rights and privileges (i.e. free health care, free elementary and secondary education, etc.) with three (3) exceptions:</p>
                    <ol style="list-style-type:lower-roman">
                        <li class=Lroman>Permanent residents cannot vote;</li>
                        <li class=Lroman>Permanent residents cannot hold a Canadian passport; and</li>
                        <li class=Lroman>Permanent residents can be deported for certain criminal convictions.</li>
                    </ol>
                    <p><a href="#top">Back to top</a></p>
                </blockquote>

                <a name="6"></a>

                <p><strong>3. Once I am a permanent resident of Canada, do I have to stay in Canada?</strong></p>

                <blockquote>
                    <p>Within any five (5) year period, a permanent resident must be:</p>
                    <p>physically present in Canada for at least 730 days (two (2) years) in that five (5) year period</strong></p>
                    OR</strong></p>
                    outside of Canada, accompanying a Canadian citizen, who is his or her spouse or common-law partner or a child accompanying a parent</strong></p>
                    OR</strong></p>
                    outside of Canada, employed on a full-time basis by a Canadian business</strong></p>
                    OR</strong></p>
                    an accompanying spouse, common-law partner or child of a permanent resident, who is outside Canada and is employed on a full-time basis by a Canadian business.</p>

                    <p><a href="#top">Back to top</a></p>
                </blockquote>

