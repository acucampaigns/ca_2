                    <p>Similarly, if claiming French language ability, all principal applicants (even those born and living in a French-speaking country) must now undergo the TEF (<a href="../www.fda.ccip.fr/default.htm">http://www.fda.ccip.fr/</a>). This is the only French language test that is acceptable to Citizenship and Immigration Canada officials.</p>
