                <h2>Important information for the sponsor before completing the Spouse/Partner/Dependent Child Questionnaire:</h2>

                <p>You can sponsor a person as your <strong>spouse</strong> if that person is of the opposite or same sex, sixteen (16) years of age or older and married to you. The marriage must be legally recognized in the country where it took place and in Canada.</p>

                <p>You can sponsor a person as your <strong>common-law partner</strong> if that person is of the opposite or same sex, has cohabited in a conjugal relationship with you for a period of at least one (1) year and is still cohabiting with you.</p>

                <p>You can sponsor a person as a <strong>conjugal partner</strong> if that person is residing outside Canada and you have maintained with this person a conjugal relationship for at least one (1) year, but you have not cohabited with him or her for at least twelve (12) continuous months. In order for a conjugal partner sponsorship to be approved, it will be necessary to demonstrate why the couple is unable to be legally married and why the couple is unable to reside together for at least twelve (12) continuous months. Note: Once the sponsorship Application is submitted, the immigrant should not enter Canada as a visitor during the processing of the Application. If your sponsorship is successful, your conjugal partner becomes a permanent resident of Canada but cannot exercise any rights or privileges associated with common-law status until you have cohabited for at least one (1) year.</p>

                <p>You can sponsor a person as a <strong>dependent child</strong> if that person is under age nineteen (19) or over age nineteen (19) and mentally or physically disabled and dependent on his/her parent(s).</p>

                <p>You cannot sponsor a person as your spouse, common-law partner or conjugal partner if:</p>
                <ul>
                    <li>that person is under sixteen (16) years of age;</li>
                    <li>you are a permanent resident and at the time you made your Application for permanent residence, that person was a non-accompanying family member, former spouse or common-law partner and was not examined;</li>
                    <li>you previously sponsored another spouse, a common-law partner or a conjugal partner and three (3) years have not passed since that spouse, common-law partner or conjugal partner became a permanent resident.</li>
                </ul>
                    <p>Further, you cannot sponsor a person as your spouse if:</p>
                <ul>
                    <li>you or this person were the spouse of another person at the time of your marriage;</li>
                    <li>you have lived separate and apart from this person for at least one (1) year and are in a common-law relationship with another person.</li>
                </ul>
                <p>You may be eligible to sponsor if:</p>
                <ul>
                    <li>the person you want to sponsor is a Member of the Family Class. If he or she is not, you will be found not to be a sponsor;</li>
                    <li>you are eighteen (18) years of age or older;</li>
                    <li>you are a Canadian citizen or permanent resident;</li>
                    <li>you reside in Canada (Canadian citizens not residing in Canada may sponsor their spouse, common-law or conjugal partner and/or dependent children who have no dependent children of their own. They must satisfy Immigration officials that they will reside in Canada at the time their sponsored spouse, common-law or conjugal partner and/or children become permanent residents of Canada. Canadians travelling abroad as tourists are not considered to be residing outside Canada);</li>
                    <li>you sign an undertaking promising to provide for the basic requirements of the person being sponsored and, if applicable, his or her dependent children;</li>
                    <li>you and the person being sponsored sign an Agreement that confirms that each of you understands your mutual obligations and responsibilities.</li>
                </ul>

                <p>There is no minimum income that you must earn in order to be eligible to sponsor.</p>

                <p><strong>Please Note:</strong> Once you submit your completed Online Assessment Questionnaire, you should immediately receive an e-mail, confirming the personal data you supplied. If you do not receive such an e-mail, you likely provided an invalid e-mail address, in which case you are asked to contact us at askus@akcanada.com and provide an alternate e-mail address that we may use to contact you.</p>


